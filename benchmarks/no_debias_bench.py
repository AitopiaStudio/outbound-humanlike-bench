def get_no_debias_prompt(scene_type: str) -> str:
    """根据场景返回无去偏版本的评分 prompt"""
    mapping = {
        "customerservice": CUSTOMERSERVICE_NO_DEBIAS_PROMPT,
        "sales": SALES_NO_DEBIAS_PROMPT,
    }
    if scene_type not in mapping:
        raise ValueError(f"消融实验目前只支持 customerservice 和 sales，不支持: {scene_type}")
    return mapping[scene_type]
