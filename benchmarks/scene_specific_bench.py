def get_scene_benchmark(scene_type: str) -> Dict[str, EvaluationDimension]:
    """根据场景类型返回对应的维度配置"""
    if scene_type == "sales":
        # 销售型使用专项 benchmark，转换为统一的 EvaluationDimension 格式
        from benchmarks.sales_humanlike_bench import SALES_DIMENSIONS, SalesDimension
        result = {}
        for k, v in SALES_DIMENSIONS.items():
            result[k] = EvaluationDimension(
                name=v.name,
                description=v.description,
                score_2_criteria=v.score_2_criteria,
                score_2_features=v.score_2_features,
                score_1_criteria=v.score_1_criteria,
                score_1_features=v.score_1_features,
                score_0_criteria=v.score_0_criteria,
                score_0_features=v.score_0_features,
                weight=v.weight,
                veto_condition=v.penalty_rules if v.penalty_rules else None,
            )
        return result

    mapping = {
        "customerservice": CUSTOMERSERVICE_DIMENSIONS,
        "notification":    NOTIFICATION_DIMENSIONS,
        "returnvisit":     RETURNVISIT_DIMENSIONS,
    }
    if scene_type not in mapping:
        raise ValueError(f"未知场景类型: {scene_type}，可选: {list(mapping.keys()) + ['sales']}")
    return mapping[scene_type]


