def print_weight_comparison():
    """打印三个场景的权重对比，便于快速理解设计意图"""

    scenes = [
        ("客服型", CUSTOMERSERVICE_DIMENSIONS),
        ("通知型", NOTIFICATION_DIMENSIONS),
        ("回访型", RETURNVISIT_DIMENSIONS),
    ]

    all_dims = set()
    for _, dims in scenes:
        all_dims.update(dims.keys())

    print("=" * 80)
    print("三场景 Benchmark 权重对比")
    print("=" * 80)
    print(f"{'维度':<18}", end="")
    for name, _ in scenes:
        print(f"  {name:^8}", end="")
    print()
    print("-" * 80)

    for dim in sorted(all_dims):
        print(f"{dim:<18}", end="")
        for _, dims in scenes:
            if dim in dims:
                w = dims[dim].weight
                veto = " ⚡" if dims[dim].veto_condition else ""
                print(f"  {w:^6.1f}{veto:2}", end="")
            else:
                print(f"  {'—':^8}", end="")
        print()

    print("=" * 80)
    print("⚡ = 该维度在此场景中有专属 Veto 条件")
    print()

    for scene_name, dims in scenes:
        total_w = sum(d.weight for d in dims.values())
        veto_dims = [d.name for d in dims.values() if d.veto_condition]
        print(f"{scene_name}：{len(dims)}个维度，总权重 {total_w:.1f}")
        print(f"  专属 Veto 维度：{', '.join(veto_dims) if veto_dims else '无'}")
    print("=" * 80)


